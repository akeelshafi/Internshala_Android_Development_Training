fun main(){
    val number1 = 25
    val number2 = 25

    var result:Int

    // add two numbers
    result = number1+number2
    println(result)

    // subtracts two numbers
    result = number1-number2
    println(result)

    // multiply two numbers
    result = number1*number2
    println(result)

    // divide two numbers
    result = number1/number2
    println(result)

    //gives reminder of two numbers
    result= number1%number2
    println(result)
}
